class Planets(Base):
    __tablename__ = 'planets'
    # Here we define columns for the table address.
    # Notice that each column is also a normal Python instance attribute.
    id = Column(Integer, primary_key=True)
    planet_name = Column(String(250))
    planet_population = Column(Integer)
    planet_terrain = Column(String(250))
    user_id = Column(Integer, ForeignKey('users.id'))
    users = relationship(Users)

class Characters(Base):
    __tablename__ = 'characters'
    # Here we define columns for the table address.
    # Notice that each column is also a normal Python instance attribute.
    id = Column(Integer, primary_key=True)
    character_name = Column(String(250))
    character_gender = Column(String(250))
    character_eyesColor = Column(String(250))
    character_hairColor = Column(String(250))
    user_id = Column(Integer, ForeignKey('users.id'))
    users = relationship(Users)

class Favorites(Base):
    __tablename__ = 'favorites'
    # Here we define columns for the table address.
    # Notice that each column is also a normal Python instance attribute.
    id = Column(Integer, primary_key=True)
    user_id = Column(Integer, ForeignKey('users.id'))
    planet_id = Column(Integer, ForeignKey('planets.id'))
    character_id = Column(Integer, ForeignKey('characters.id'))
    users = relationship(Users)
    planets = relationship(Planets)
    characters = relationship(Characters)


    def to_dict(self):
        return {}

